class VertexComparator implements Comparator<Graph>
{ 
   public int compare (Graph a, Graph b)
   {
       return a.getVertex().compareTo(b.getVertex());
   }
}   

class EdgeComparator implements Comparator<Edge>
{
    public int compare(Edge a, Edge b)
    {
        if(a.getV1().getVertex().equals(b.getV1().getVertex()))
            return a.getV2().getVertex().compareTo(b.getV2().getVertex());
        else
           return a.getV1().getVertex().compareTo(b.getV1().getVertex());
    }
}

