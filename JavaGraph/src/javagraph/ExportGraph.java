    private void uuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uuActionPerformed
       
       String output = vv.getText().trim();
       
       ArrayList<Graph> arr = new ArrayList<Graph>(vet);
       ArrayList<Edge> temp = new ArrayList<Edge>(edt);
       
       Collections.sort(arr , new VertexComparator());
       Collections.sort(temp , new EdgeComparator());
       
       try
       {
           FileWriter writer = new FileWriter(output,true);
           for(int i = 0; i < arr.size(); i++)
           {
               String word = arr.get(i).getVertex() + " " + arr.get(i).getX() + " " + arr.get(i).getY();
               writer.write(word);
               writer.write(System.getProperty("line.separator"));
           }
           for(int i = 0; i < temp.size(); i++)
           {
               String worde = temp.get(i).getV1().getVertex() + " " +temp.get(i).getV2().getVertex()+ " " + temp.get(i).getWeight();
               writer.write(worde);
               writer.write(System.getProperty("line.separator"));
           }
           writer.close();
           JOptionPane.showMessageDialog(null , "Succesfully Written");
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null , e.getMessage());
       }
    }//GEN-LAST:event_uuActionPerformed

